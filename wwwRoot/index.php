					<h2><a name="Organs"></a>How are Our Emotions Stored in the Organs?</h2>
					<p>It is well documented that our emotions have a great impact on how our body functions, and this is greatly due to how receptive organs are to our feelings. Different emotions affect different organs. Emotional reactions can translate into simple spasms in the gallbladder to heartburn, vomiting, feeling faint, ulcers, and serious diseases. When the brain receives negative emotions it sends tension to the related organ, the organ then sends tension back to the brain resulting in a vicious cycle. Conversely, a physically damaged or imbalanced organ can stimulate emotional upset. VM can break this cycle, enhance the health of the organ, and restore emotional balance.</p>
