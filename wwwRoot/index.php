					<h2><a name="Pain"></a>How Can Organs Cause Pain and Dysfunction?</h2>
					<p>Your body is made up of many interrelated components such as bones, muscles, nerves, a thin connective tissue called fascia, as well as the internal organs (viscera). Your organs are in perpetual motion. When you breathe, walk and stretch, your organs move in your chest and abdomen. For example, when you take a breath, your kidneys move one inch; and with deep inhalation, they move 4 inches. In a day, they move a little over &#0189; mile. That's around 19,000 miles in a lifetime!</p>
					<p>This movement of organs is transmitted through fascia to other structures of the body. When you are healthy, all the structures move with an interconnected fluidity. All of this movement is important as it influences activities throughout the body from the tiniest cellular pulsations to rhythmic contractions of the heart and blood flow. Optimum health relies on a harmonious relationship between the motions of the organs and other structures of the body.</p>
					<p>There are many reasons for an organ to lose its mobility: physical traumas, surgeries, sedentary lifestyle, infections, pollution, bad diet, poor posture and pregnancy/delivery. When an organ is no longer freely mobile but is fixed to another structure, the body is forced to compensate. This disharmony creates fixed, abnormal points of tension and the chronic irritation gives way to functional and structural problems throughout the body – musculoskeletal, vascular, nervous, urinary, respiratory and digestive, to name a few.</p>
					<p>Imagine scar tissue around the lungs. Because of the pull of the adhesion, with every breath, the movement patterns of the nearby structures would be altered. This could shift rib motion creating pulls on the spine. These restrictions might then show up as mid-back and neck pain, as well as limited motion in the shoulder. This scenario highlights just one of hundreds of possible ramifications of a small dysfunction - magnified by thousands of repetitions each day. This also explains how pain can often be far removed from the actual cause.</p>
