					<h2><a name="HowVM"></a>How Did Visceral Manipulation Begin?</h2>
					<p>Jean-Pierre Barral is an Osteopath and Registered Physical Therapist from France. He first became interested in the movement of the body (biomechanics) while working at the Lung Disease Hospital in Grenoble, France with Dr. Arnaud, a recognized specialist in lung diseases and a master of cadaver dissection. Barral was able to follow patterns of stress in the tissues of cadavers as he studied biomechanics in living subjects. This introduced him to the visceral system, its potential to promote lines of tension within the body, and the notion that tissues have memory. Barral's clinical work with the viscera led to his development of this form of manual therapy that focuses on the internal organs, their fascial environment and the potential influence on structural and physiological dysfunctions. The term he coined for this therapy was Visceral Manipulation.</p>
					<p>Jean-Pierre Barral began teaching Visceral Manipulation in the United States in 1985. Since then he has trained a team of International Instructors that teach Visceral Manipulation seminars around the world. He has authored numerous textbooks for healthcare professionals, and has also authored a book for the general public, <a href="http://shop.iahe.com/s.nl/it.A/id.7/.f?sc=7&amp;category=538" target="_blank">Understanding the Messages of Your Body</a>, which discusses the important link between our organs and our emotions.</p>
