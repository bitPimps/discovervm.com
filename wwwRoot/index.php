					<h2><a name="WhatIsVM"></a>What is Visceral Manipulation</h2>
					<p>&quot;Viscera&quot; relates to the internal organs of the body, such as the liver, kidneys and intestines. Visceral Manipulation is a gentle manual therapy that aids your body's ability to release restrictions and unhealthy compensations that cause pain and dysfunction. Visceral Manipulation, or VM, does not focus solely on the site of pain or dysfunction, but evaluates the entire body to find the source of the problem. The VM therapist feels for altered or decreased motion within the viscera, as well as restrictive patterns throughout the body and then applies VM techniques. VM therapy re-establishes the body's ability to adapt and restore itself to health.</p>
